package objetos;

public class Data {
	int dia;
	int mes;
	int ano;
	
	public int getdia() {
		return dia;
	}
	
	public void setdia(int dia) {
		this.dia = dia;
	}
	
	public int getmes() {
		return mes;
	}
	
	public void setmes(int mes) {
		this.mes = mes;
	}
	
	public int getano() {
		return ano;
	}
	
	public void setano(int ano) {
		this.ano = ano;
	}
}
